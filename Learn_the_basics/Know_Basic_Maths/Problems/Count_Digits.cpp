class Solution{
public:
    int evenlyDivides(int N){
        //code here
        int count = 0, X = N, dig;
        while(X > 0){
            dig = X%10;
            if(dig != 0 && N % dig == 0){
                count++;
            }
            X = X / 10;
        }
        return count;
    }
};

